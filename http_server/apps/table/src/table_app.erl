-module(table_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case node() of
        'http_server@127.0.0.1' ->
            table_create:init();
        _ ->
            table_create:start()
    end,
    % table_create:start(),
    table_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

